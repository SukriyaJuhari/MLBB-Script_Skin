    /************************************************************************
     * ELEMENTS
     ************************************************************************/
    const heroGallery = document.getElementById('heroGallery');
    
    const replaceDropdown = document.getElementById('replaceDropdown');
    const replaceList = document.getElementById('replaceList');
    const replacePreviewImg = document.querySelector('#replacePreview img');
    const replaceText = document.getElementById('replaceText');
    
    const skinDropdown = document.getElementById('skinDropdown');
    const skinList = document.getElementById('skinList');
    const skinPreviewImg = document.querySelector('#skinPreview img');
    const skinText = document.getElementById('skinText');
    
    const downloadBtn = document.getElementById('downloadBtn');
    const fileTitle = document.getElementById('fileTitle');
    
    let selectedHero = null;
    let selectedReplace = null;
    let selectedScript = null;
    
    
    /************************************************************************
     * 1)filter role
     ************************************************************************/
    
    document.getElementById("roleSelect").addEventListener("change", function() {
      const selectedRole = this.value;
      
      heroGallery.innerHTML = "";
      
      Object.keys(dataHero).forEach(hero => {
        if (selectedRole && dataHero[hero].role !== selectedRole) return;
        
        const box = document.createElement('div');
        box.className = 'hero-item';
        box.dataset.hero = hero;
        box.innerHTML = `<img src="${dataHero[hero].icon}">`;
        
        box.onclick = () => selectHero(hero);
        
        heroGallery.appendChild(box);
      });
    });
    
    
    
    /************************************************************************
     * 2)Render hero gallery 
     ************************************************************************/
    
    
    
    function renderHeroGallery() {
      heroGallery.innerHTML = '';
      Object.keys(dataHero).forEach(hero => {
        const box = document.createElement('div');
        box.className = 'hero-item';
        box.dataset.hero = hero;
        box.innerHTML = `<img src="${dataHero[hero].icon}">`;
        
        box.onclick = () => selectHero(hero);
        heroGallery.appendChild(box);
      });
    }
    
    /************************************************************************
     * 3) Select Hero
     ************************************************************************/
    function selectHero(hero) {
      selectedHero = hero;
      selectedReplace = null;
      selectedScript = null;
      
      document.querySelectorAll('.hero-item').forEach(el => el.classList.remove('hero-active'));
      document.querySelector(`.hero-item[data-hero="${hero}"]`).classList.add('hero-active');
      
      fileTitle.innerText = `Script Skin MLBB â€” ${hero}`;
      
      populateReplaceList();
      
      const firstReplace = Object.keys(dataHero[hero].replace)[0];
      selectReplace(firstReplace);
    }
    
    /************************************************************************
     * 4) Populate Replace List
     ************************************************************************/
    function populateReplaceList() {
      replaceList.innerHTML = '';
      
      const items = dataHero[selectedHero].replace;
      Object.keys(items).forEach(key => {
        const item = document.createElement('div');
        item.className = 'item-list';
        
        item.innerHTML = `
      <div class="thumb"><img src="${items[key].thumb}"></div>
      <div class="meta">
        <div class="title">${key}</div>
        <div class="sub">Replace</div>
      </div>
    `;
        
        item.onclick = () => {
          selectReplace(key);
          toggleList(replaceList);
        };
        
        replaceList.appendChild(item);
      });
    }
    
    /************************************************************************
     * 5) Select Replace => show scripts for this replace only
     ************************************************************************/
    function selectReplace(key) {
      selectedReplace = key;
      
      const data = dataHero[selectedHero].replace[key];
      
      replacePreviewImg.src = data.thumb;
      replaceText.innerText = key;
      
      populateSkinList(data.scripts);
      
      const firstScript = Object.keys(data.scripts)[0] || null;
      if (firstScript) selectSkin(firstScript);
      else {
        selectedScript = null;
        skinText.innerText = "No Script";
        skinPreviewImg.src = "";
      }
      
      updateDownloadLink();
    }
    
    /************************************************************************
     * 6) Populate Script List (based on selected replace)
     ************************************************************************/
    function populateSkinList(scriptObj) {
      skinList.innerHTML = "";
      
      Object.keys(scriptObj).forEach(key => {
        const item = document.createElement('div');
        item.className = 'item-list';
        
        item.innerHTML = `
      <div class="thumb"><img src="${scriptObj[key]}"></div>
      <div class="meta"><div class="title">${key}</div><div class="sub">Script</div></div>
    `;
        
        item.onclick = () => {
          selectSkin(key);
          toggleList(skinList);
        };
        
        skinList.appendChild(item);
      });
    }
    
    /************************************************************************
     * 7) Select Script
     ************************************************************************/
    function selectSkin(key) {
      selectedScript = key;
      
      const url = dataHero[selectedHero].replace[selectedReplace].scripts[key];
      skinPreviewImg.src = url;
      skinText.innerText = key;
      
      updateDownloadLink();
    }
    
    /************************************************************************
     * 8) Dropdown Toggle
     ************************************************************************/
    function toggleList(listEl) {
      if (listEl !== replaceList) replaceList.classList.remove("active");
      if (listEl !== skinList) skinList.classList.remove("active");
      
      listEl.classList.toggle("active");
    }
    
    document.getElementById("replaceCurrent").onclick = e => {
      e.stopPropagation();
      toggleList(replaceList);
    };
    
    document.getElementById("skinCurrent").onclick = e => {
      e.stopPropagation();
      toggleList(skinList);
    };
    
    document.addEventListener("click", ev => {
      if (!replaceDropdown.contains(ev.target)) replaceList.classList.remove("active");
      if (!skinDropdown.contains(ev.target)) skinList.classList.remove("active");
    });
    
    /************************************************************************
     * 9) Update Download URL
     ************************************************************************/
    function updateDownloadLink() {
      if (!selectedHero || !selectedReplace || !selectedScript) {
        downloadBtn.href = "#";
        downloadBtn.setAttribute("aria-disabled", "true");
        return;
      }
      
      const heroParam = encodeURIComponent(selectedHero);
      const replaceParam = encodeURIComponent(selectedReplace);
      const scriptParam = encodeURIComponent(selectedScript);
      
      const url = `https://skripinaja.blogspot.com/p/wait.html?file=${heroParam}_${replaceParam}=${scriptParam}`;
      downloadBtn.href = url;
      console.log(url)
      downloadBtn.removeAttribute("aria-disabled");
    }
    
    /************************************************************************
     * INIT
     ************************************************************************/
    renderHeroGallery();
    selectHero(Object.keys(dataHero)[0]);